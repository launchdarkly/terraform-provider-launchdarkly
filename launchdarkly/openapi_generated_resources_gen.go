func (r *GeneratedProjectResource) hydrateState(ctx context.Context, data *GeneratedProjectResourceModel, diags *diag.Diagnostics) bool {
	projectKey := data.Key.ValueString()
	var project *ldapi.Project
	var res *http.Response
	var err error

	err = r.client.withConcurrency(r.client.ctx, func() error {
		project, res, err = getFullProject(r.client, projectKey)
		return err
	})
	if err != nil {
		if isStatusNotFound(res) {
			diags.AddWarning("Generated project not found", fmt.Sprintf("Failed to find project %q, removing from state.", projectKey))
			return false
		}
		diags.AddError("Unable to read generated project", fmt.Sprintf("Failed to get project %q: %s", projectKey, handleLdapiErr(err)))
		return false
	}

	tagsSet, tagsDiags := generatedStringSliceToSet(ctx, project.Tags)
	diags.Append(tagsDiags...)
	data.ID = types.StringValue(project.Key)
	data.Key = types.StringValue(project.Key)
	data.Name = types.StringValue(project.Name)
	data.IncludeInSnippet = types.BoolValue(project.IncludeInSnippetByDefault)
	data.DefaultClientSideAvailability = generatedProjectClientSideAvailabilityToModel(project.DefaultClientSideAvailability)
	data.Tags = tagsSet

	viewSettings, viewErr := getProjectViewSettings(ctx, r.client, projectKey)
	if viewErr != nil {
		diags.AddWarning("Unable to read project view-association settings", fmt.Sprintf("Failed to fetch view-association settings for project %q: %s", projectKey, viewErr))
	} else {
		data.RequireViewAssociationForNewFlags = types.BoolValue(viewSettings.RequireViewAssociationForNewFlags)
		data.RequireViewAssociationForNewSegments = types.BoolValue(viewSettings.RequireViewAssociationForNewSegments)
	}

	currentEnvironmentOrder := make([]string, 0, len(data.Environments))
	for _, env := range data.Environments {
		if env.Key.IsNull() || env.Key.IsUnknown() {
			continue
		}
		currentEnvironmentOrder = append(currentEnvironmentOrder, env.Key.ValueString())
	}

	environmentMap := make(map[string]generatedProjectEnvironmentModel, len(project.Environments.Items))
	for _, env := range project.Environments.Items {
		model, modelDiags := generatedProjectEnvironmentToModel(ctx, env)
		diags.Append(modelDiags...)
		environmentMap[env.Key] = model
	}

	ordered := make([]generatedProjectEnvironmentModel, 0, len(environmentMap))
	added := make(map[string]bool, len(environmentMap))
	for _, key := range currentEnvironmentOrder {
		if env, ok := environmentMap[key]; ok {
			ordered = append(ordered, env)
			added[key] = true
		}
	}

	remainingKeys := make([]string, 0, len(environmentMap))
	for key := range environmentMap {
		if !added[key] {
			remainingKeys = append(remainingKeys, key)
		}
	}
	sort.Strings(remainingKeys)
	for _, key := range remainingKeys {
		ordered = append(ordered, environmentMap[key])
	}

	data.Environments = ordered
	return !diags.HasError()
}

