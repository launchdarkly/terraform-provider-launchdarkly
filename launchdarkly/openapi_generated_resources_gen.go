func (r *GeneratedProjectResource) applyUpdates(ctx context.Context, plan *GeneratedProjectResourceModel, state *GeneratedProjectResourceModel, isCreate bool, diags *diag.Diagnostics) bool {
	if len(plan.DefaultClientSideAvailability) > 0 && !plan.IncludeInSnippet.IsNull() && !plan.IncludeInSnippet.IsUnknown() {
		diags.AddError(
			"Invalid generated project configuration",
			"Only one of `include_in_snippet` and `default_client_side_availability` can be set.",
		)
		return false
	}

	tags, tagDiags := generatedStringSetToSlice(ctx, plan.Tags)
	diags.Append(tagDiags...)
	if diags.HasError() {
		return false
	}

	projName := plan.Name.ValueString()
	clientSideAvailability := generatedProjectClientSideAvailabilityFromModel(plan.DefaultClientSideAvailability, plan.IncludeInSnippet)
	patch := []ldapi.PatchOperation{
		patchReplace("/name", &projName),
		patchReplace("/tags", &tags),
		patchReplace("/defaultClientSideAvailability", &clientSideAvailability),
	}

	projectKey := plan.Key.ValueString()
	var err error
	err = r.client.withConcurrency(r.client.ctx, func() error {
		_, _, err = r.client.ld.ProjectsApi.PatchProject(r.client.ctx, projectKey).PatchOperation(patch).Execute()
		return err
	})
	if err != nil {
		diags.AddError("Unable to update generated project", fmt.Sprintf("Failed to update project %q: %s", projectKey, handleLdapiErr(err)))
		return false
	}

	flagsRequired := generatedProjectBoolValue(plan.RequireViewAssociationForNewFlags, false)
	segmentsRequired := generatedProjectBoolValue(plan.RequireViewAssociationForNewSegments, false)
	flagsChanged := isCreate
	segmentsChanged := isCreate
	if state != nil {
		flagsChanged = flagsRequired != generatedProjectBoolValue(state.RequireViewAssociationForNewFlags, false)
		segmentsChanged = segmentsRequired != generatedProjectBoolValue(state.RequireViewAssociationForNewSegments, false)
	}
	if flagsChanged || segmentsChanged {
		if viewErr := patchProjectViewSettings(ctx, r.client, projectKey, flagsRequired, segmentsRequired, flagsChanged, segmentsChanged); viewErr != nil {
			diags.AddError("Unable to update generated project view-association settings", fmt.Sprintf("Failed to update view-association settings for project %q: %s", projectKey, viewErr))
			return false
		}
	}

	project, _, err := getFullProject(r.client, projectKey)
	if err != nil {
		diags.AddError("Unable to load generated project environments", fmt.Sprintf("Failed to get project %q before applying environment updates: %s", projectKey, handleLdapiErr(err)))
		return false
	}

	existingEnvironmentKeys := make(map[string]bool, len(project.Environments.Items))
	for _, env := range project.Environments.Items {
		existingEnvironmentKeys[env.Key] = true
	}

	stateEnvironmentMap := map[string]generatedProjectEnvironmentModel{}
	if state != nil {
		stateEnvironmentMap = generatedProjectEnvironmentModelMap(state.Environments)
	}

	desiredEnvironmentKeys := make(map[string]bool, len(plan.Environments))
	for _, env := range plan.Environments {
		envKey := env.Key.ValueString()
		desiredEnvironmentKeys[envKey] = true

		if !existingEnvironmentKeys[envKey] {
			post := generatedProjectEnvironmentPostFromModel(env)
			err = r.client.withConcurrency(r.client.ctx, func() error {
				_, _, err = r.client.ld.EnvironmentsApi.PostEnvironment(r.client.ctx, projectKey).EnvironmentPost(post).Execute()
				return err
			})
			if err != nil {
				diags.AddError("Unable to create generated project environment", fmt.Sprintf("Failed to create environment %q in project %q: %s", envKey, projectKey, handleLdapiErr(err)))
				return false
			}
		}

		var oldEnv *generatedProjectEnvironmentModel
		if previous, ok := stateEnvironmentMap[envKey]; ok {
			previousCopy := previous
			oldEnv = &previousCopy
		}
		envPatch, envPatchDiags, patchErr := generatedProjectEnvironmentPatchesFromModel(ctx, oldEnv, env)
		diags.Append(envPatchDiags...)
		if diags.HasError() {
			return false
		}
		if patchErr != nil {
			diags.AddError("Unable to build generated project environment patch", fmt.Sprintf("Failed to build patch for environment %q in project %q: %s", envKey, projectKey, patchErr))
			return false
		}
		err = r.client.withConcurrency(r.client.ctx, func() error {
			_, _, err = r.client.ld.EnvironmentsApi.PatchEnvironment(r.client.ctx, projectKey, envKey).PatchOperation(envPatch).Execute()
			return err
		})
		if err != nil {
			diags.AddError("Unable to update generated project environment", fmt.Sprintf("Failed to patch environment %q in project %q: %s", envKey, projectKey, handleLdapiErr(err)))
			return false
		}
	}

	for envKey := range stateEnvironmentMap {
		if !desiredEnvironmentKeys[envKey] {
			err = r.client.withConcurrency(r.client.ctx, func() error {
				_, err = r.client.ld.EnvironmentsApi.DeleteEnvironment(r.client.ctx, projectKey, envKey).Execute()
				return err
			})
			if err != nil {
				diags.AddError("Unable to delete generated project environment", fmt.Sprintf("Failed to delete environment %q in project %q: %s", envKey, projectKey, handleLdapiErr(err)))
				return false
			}
		}
	}

	return !diags.HasError()
}

func (r *GeneratedProjectResource) Create(ctx context.Context, req resource.CreateRequest, resp *resource.CreateResponse) {
	var plan GeneratedProjectResourceModel
	resp.Diagnostics.Append(req.Plan.Get(ctx, &plan)...)
	if resp.Diagnostics.HasError() {
		return
	}

	environmentPosts := make([]ldapi.EnvironmentPost, len(plan.Environments))
	for i, env := range plan.Environments {
		environmentPosts[i] = generatedProjectEnvironmentPostFromModel(env)
	}

	projectBody := ldapi.ProjectPost{
		Key:          plan.Key.ValueString(),
		Name:         plan.Name.ValueString(),
		Environments: environmentPosts,
	}
	var err error
	err = r.client.withConcurrency(r.client.ctx, func() error {
		_, _, err = r.client.ld.ProjectsApi.PostProject(r.client.ctx).ProjectPost(projectBody).Execute()
		return err
	})
	if err != nil {
		if !isTimeoutError(err) {
			resp.Diagnostics.AddError("Unable to create generated project", fmt.Sprintf("Failed to create project %q: %s", plan.Key.ValueString(), handleLdapiErr(err)))
			return
		}
		log.Printf("[DEBUG] timeout creating generated project %q", plan.Key.ValueString())
	}

	if !r.applyUpdates(ctx, &plan, nil, true, &resp.Diagnostics) {
		return
	}

	if !r.hydrateState(ctx, &plan, &resp.Diagnostics) {
		if resp.Diagnostics.HasError() {
			return
		}
	}
	resp.Diagnostics.Append(resp.State.Set(ctx, &plan)...)
}

